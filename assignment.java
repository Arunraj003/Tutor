/*
    +=    a+=10    a=a+1
    -=    a-=10    a=a-1
    *=    a*=10    a=a*1
    /=    a/=10    a=a/1
    %=    a%=10    a=a%1  
*/

public class assignment {
    public static void main(String args[]) {
        int a = 123;
        System.out.println(a);
        a += 10; // a = a + 1
        System.out.println(a);
        a -= 10;
        System.out.println(a);
        a *= 10;
        System.out.println(a);
        a /= 10;
        System.out.println(a);
        a %= 10;
        System.out.println(a);

    }
}
