//11.unary
public class unary {
  public static void main(String args[]){

    // unary
    int a = 10;
    //a++; //a=a+1
    System.out.println(a++); //post
    System.out.println(a--); // post
    System.out.println(a);
    System.out.println(++a); // pre
    System.out.println(--a); // pre
  }  
}
